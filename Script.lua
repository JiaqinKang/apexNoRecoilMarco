		end



		if version == 1 then



			OutputLogMessage(ToUTF8(tbl3)..ToUTF8(text_wxtzsj).."\n")



		end



	end



	return tbl1;



end







function getTblLen(tbl)



	if type(tbl) ~= "table" then



		OutputLogMessage("table erron")



		return



	end



	local len = 0



	for k, v in pairs(tbl) do



		len = len + 1



	end



	return len



end







