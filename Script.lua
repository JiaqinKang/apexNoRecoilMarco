function SetGun_offset(tbl1,tbl2,ratio,amend)



	if ratio == nil then ratio = {1,1} end



	if amend == nil then amend = {0,0} end



	local tbl3 = {}



	for i, t1 in ipairs(tbl1) do



		s1,s2,s3,s4 = t1[1],t1[2],t1[3],t1[4]



		s2 = s2 * ratio[1]



		s3 = s3 * ratio[2]



		if s2 > 0 then



			s2 = s2 + amend[1]



		elseif s2 < 0 then



			s2 = s2 - amend[1]



		end



		if s3 > 0 then



			s3 = s3 + amend[2]



		elseif s3 < 0 and amend[2] < 0 then



			s3 = s3 - amend[2]



		end



		s2 = s2 + tbl2[i][2]



		s3 = s3 + tbl2[i][3]



		table.insert(tbl3,{s1,s2,s3,s4})



	end



	return tbl3



end







function setTbl(tbl1,tbl2,tbl3)



